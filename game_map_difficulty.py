import numpy as np
from collections import deque

def compute_jump_complexity(grid):
    """计算跳跃复杂度（Jump Complexity）"""
    height, width = len(grid), len(grid[0])
    start_x, start_y = None, None
    for y in range(height):
        for x in range(width):
            if grid[y][x] == '@':
                start_x, start_y = x, y
                break
        if start_x is not None:
            break
    
    if start_x is None:
        return 0  # 没有起点

    jump_complexity = 0
    visited = set()
    queue = deque([(start_x, start_y, 0)])  # (x, y, jumps)
    
    while queue:
        x, y, jumps = queue.popleft()
        if (x, y) in visited:
            continue
        visited.add((x, y))

        # 向上跳跃
        for dy in range(1, 4):  # 假设最大跳跃高度为 3
            new_y = y - dy
            if 0 <= new_y < height and grid[new_y][x] in "x!":
                jump_complexity += dy  # 跳跃高度越高，难度越大
                queue.append((x, new_y, jumps + 1))

        # 向左右移动
        for dx in [-1, 1]:
            new_x = x + dx
            if 0 <= new_x < width and grid[y][new_x] in "x!":
                queue.append((new_x, y, jumps))

    return jump_complexity

def compute_lava_number(grid):
    """计算熔岩数量（Lava Number）"""
    return sum(row.count('!') for row in grid)

def compute_coin_complexity(grid):
    """计算金币复杂度（Coin Complexity）"""
    height = len(grid)
    coin_count = 0
    total_height = 0
    
    for y in range(height):
        row = grid[y]
        coin_count += row.count('o')
        total_height += row.count('o') * y  # 金币越高，难度越大
    
    return total_height // coin_count if coin_count > 0 else 0

def compute_solution_length(grid):
    """计算解法长度（Solution Length）"""
    height, width = len(grid), len(grid[0])
    start_x, start_y = None, None
    
    for y in range(height):
        for x in range(width):
            if grid[y][x] == '@':
                start_x, start_y = x, y
                break
        if start_x is not None:
            break
    
    if start_x is None:
        return 0  # 没有起点
    
    # BFS 计算最短路径
    queue = deque([(start_x, start_y, 0)])
    visited = set()
    
    while queue:
        x, y, dist = queue.popleft()
        if (x, y) in visited:
            continue
        visited.add((x, y))
        
        if y == 0:  # 走到顶部，认为是终点
            return dist
        
        for dx, dy in [(0, -1), (-1, 0), (1, 0)]:
            new_x, new_y = x + dx, y + dy
            if 0 <= new_x < width and 0 <= new_y < height and grid[new_y][new_x] in "x!@o":
                queue.append((new_x, new_y, dist + 1))
    
    return len(grid)  # 若未找到终点，返回最大值

def compute_platform_difficulty(grid):
    """计算平台布局难度（Platform Layout Difficulty）"""
    platform_count = sum(row.count('x') for row in grid)
    platform_gap = 0
    width = len(grid[0])
    print(width)
    for y in range(len(grid)):
        x_positions = [x for x in range(width) if grid[y][x] == 'x']
        if len(x_positions) > 1:
            platform_gap += max(x_positions) - min(x_positions)
    
    return platform_count + platform_gap // len(grid)  # 平台数量 + 平均间距

def analyze_maps(maps):
    """计算所有地图的难度指标"""
    results = []
    for i, grid in enumerate(maps, 1):
        jump_complexity = compute_jump_complexity(grid)
        lava_number = compute_lava_number(grid)
        coin_complexity = compute_coin_complexity(grid)
        solution_length = compute_solution_length(grid)
        platform_difficulty = compute_platform_difficulty(grid)
        
        results.append({
            "Map": i,
            "Jump Complexity (J)": jump_complexity,
            "Lava Number (L)": lava_number,
            "Coin Complexity (C)": coin_complexity,
            "Solution Length (S)": solution_length,
            "Platform Layout Difficulty (P)": platform_difficulty,
            'Total Difficulty': jump_complexity + lava_number + coin_complexity + solution_length + platform_difficulty
        })
    
    return results

